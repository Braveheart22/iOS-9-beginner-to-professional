func subtract (first: Int, second: Int) -> Int {
    return first - second
}

subtract(10, second: 80)

func multiply (first: Float, second: Float) -> Float {
    return first * second
}

multiply(9, second: 9)

func divide (first: Double, second: Double) -> Double {
    return first / second
}

divide(10, second: 100)

